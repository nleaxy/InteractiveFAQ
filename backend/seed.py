def init_and_seed_db():
    print("Инициализация базы данных PostgreSQL по обновленным схемам...")
    
    Base.metadata.drop_all(bind=engine)
    Base.metadata.create_all(bind=engine)
    
    db = SessionLocal()
    try:
        print("Создание администратора Александра...")
        test_user = User(
            id=1,
            name="Александр",
            email="admin@example.com",
            password_hash=hash_password("admin123")
        )
        db.add(test_user)
        db.commit()
        print("Администратор создан.")

        print("Наполнение базы проектами и статьями...")
        for b_data in ANALYST_DATA["businesses"]:
            project = FAQProject(
                id=b_data["id"],
                name=b_data["name"],
                slug=b_data["slug"],
                owner_id=test_user.id
            )
            db.add(project)
            db.flush()

            for c_data in b_data["categories"]:
                category_name = c_data["name"]

                for f_data in c_data["faq"]:
                    faq_item = FAQItem(
                        project_id=project.id,
                        category=category_name,
                        question=f_data["question"],
                        answer=f_data["answer"],
                        synonyms=f_data.get("synonyms", [])
                    )
                    db.add(faq_item)

        db.commit()
        print("База данных PostgreSQL успешно пересоздана и наполнена!")
        
    except Exception as e:
        print(f"Ошибка при работе с СУБД: {e}")
        db.rollback()
    finally:
        db.close()
        print("Процесс завершен.")

if __name__ == "__main__":
    init_and_seed_db()